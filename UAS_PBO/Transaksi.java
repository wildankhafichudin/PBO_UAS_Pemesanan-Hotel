    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:
       Date currentDate = new Date(System.currentTimeMillis());       
       try {
           String kode = txtKode.getText();                     
           String jenis = CBJenis.getSelectedItem().toString().trim();
           int harga = Integer.parseInt(txtHarga.getText().trim());
           Date tglMsk = new Date(dateMasuk.getDate().getTime());
           Date tglKlr = new Date(dateKeluar.getDate().getTime());
        Class.forName(JDBC_DRIVER);
        conn = DriverManager.getConnection(DB_URL, USER, PASS);     
        stmt = conn.createStatement();

        // Operasi db
        //String checkSql = "SELECT * FROM barang WHERE id = ?";
        String sql = "UPDATE uashotel set kode_kamar=?, jenis_kamar= ?, harga= ?, tgl_masuk = ?, tgl_keluar=? where kode_kamar=?";
        PreparedStatement ps = conn.prepareStatement(sql);
        //PreparedStatement psc = conn.prepareStatement(checkSql);
        //psc.setInt(1, id);        "UPDATE uashotel set kode_kamar=?, nama= ?, jenis = ?, tanggal_masuk = ?, tanggal_keluar=? WHERE no_kamar=?";
        ps.setString(1, kode);                
        ps.setString(2, jenis);
        ps.setInt(3, harga);
        ps.setDate(4, tglMsk);
        ps.setDate(5, tglKlr);
        ps.setString (6,kode);

        int x = ps.executeUpdate();
        
        if (x==1)
        {
            JOptionPane.showMessageDialog(this,"Data berhasil diupdate");
            txtKode.setText("");           
            CBJenis.setSelectedItem("");
            txtHarga.setText("");
            dateMasuk.setDate(currentDate);
            dateKeluar.setDate(currentDate);
            txtKode.requestFocus();
        }else
        {
            JOptionPane.showMessageDialog(this,"Data Gagal diupdate!");
        }
        

        stmt.close();
        conn.close();
    } catch (Exception e) {
    }
       showtable();
    }//GEN-LAST:event_UpdateActionPerformed

