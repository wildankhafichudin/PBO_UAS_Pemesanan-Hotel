    public void hitung()
    {
    int harga = 0;
        Date tglMsk = new Date(dateMasuk.getDate().getTime());
        Date tglKlr = new Date(dateKeluar.getDate().getTime());
        String selectedOption = (String) CBJenis.getSelectedItem();
                if (selectedOption.equals("Normal")) {
                    long selisihHari = TimeUnit.DAYS.convert(tglKlr.getTime() - tglMsk.getTime(), TimeUnit.MILLISECONDS);
                    harga = 500000*(int)selisihHari;
                } else if (selectedOption.equals("Deluxe")) {
                    long selisihHari = TimeUnit.DAYS.convert(tglKlr.getTime() - tglMsk.getTime(), TimeUnit.MILLISECONDS);
                    harga = 1000000*(int)selisihHari;
                } else if (selectedOption.equals("Suite")) {
                    long selisihHari = TimeUnit.DAYS.convert(tglKlr.getTime() - tglMsk.getTime(), TimeUnit.MILLISECONDS);
                    harga = 1500000*(int)selisihHari;
                }            
                txtHarga.setText(Integer.toString(harga));
    }
    public void showtable(){
        try{
            Class.forName(JDBC_DRIVER);	   
            conn = DriverManager.getConnection(DB_URL, USER, PASS);
            stmt = conn.createStatement();
            String sql = "SELECT * FROM uashotel";
            rs = stmt.executeQuery(sql);
            DefaultTableModel model = new DefaultTableModel();

            model.addColumn("ID Kamar");
            model.addColumn("Jenis Kamar");
            model.addColumn("Harga");
            model.addColumn("Tanggal Masuk");
            model.addColumn("Tanggal Keluar");
 
            while (rs.next()) {
                // Extract data from the result set
                String kode = rs.getString("kode_kamar");
                String jenis = rs.getString("jenis_kamar");
                int harga = rs.getInt("harga");
                Date tglMsk = rs.getDate("tgl_masuk");
                Date tglKlr = rs.getDate("tgl_keluar");

                // Add a new row to the table model
                model.addRow(new Object[]{kode, jenis, harga, tglMsk, tglKlr});
            }
            rs.close();
            conn.close();
            stmt.close();
            
            tabelData.setModel(model);
        }
        catch(Exception e){
        }
    }
      


