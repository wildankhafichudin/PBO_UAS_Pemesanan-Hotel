    private void pilihan()
    {
        try {
            conn = DriverManager.getConnection(DB_URL, USER, PASS);
            stmt = conn.createStatement();
            rs = stmt.executeQuery("SELECT id FROM user");
            while(rs.next())
            {
                CBID.addItem(rs.getString(1).toString());
            }
            
            stmt.close();
            conn.close();
            rs.close();
        } catch (SQLException e) {
            e.printStackTrace();
            
        }
    }
