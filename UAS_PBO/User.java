    private void butTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butTambahActionPerformed
        // TODO add your handling code here:
        String user = txtUser.getText().toString().trim();
        String pass = txtPass.getText().toString().trim();
        int id = Integer.parseInt(CBID.getSelectedItem().toString());
        //insert(kode,nomor,nama,jenis,harga,tglMsk,tglKlr);
            Date currentDate = new Date(System.currentTimeMillis());
    try {
        Class.forName(JDBC_DRIVER);
        conn = DriverManager.getConnection(DB_URL, USER, PASS);

        // Pengecekan kd_brg
        String checkQuery = "SELECT * FROM user WHERE username = ?";
        PreparedStatement checkStmt = conn.prepareStatement(checkQuery);        
        checkStmt.setString(1, user);        
        ResultSet rs = checkStmt.executeQuery();

        if (rs.next()) {           
            String userFromDB = rs.getString("username");            
            if (user.equals(userFromDB)) {
                JOptionPane.showMessageDialog(this,"Username sudah ada, silahkan ganti dengan yang lain.");
                return;        
            }
        }else {
        

        stmt = conn.createStatement();

        // Operasi db

        String sql = "INSERT INTO user (username,password) VALUES (?, ?)";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, user);
        ps.setString(2, pass);
                               
        int x = ps.executeUpdate();
        if (x==1)
        {
            JOptionPane.showMessageDialog(this,"Data Berhasil Ditambah");
            txtUser.setText("");
            txtPass.setText("");
            
        }else
        {
            JOptionPane.showMessageDialog(this,"Data Gagal Ditambah");
        }
        }
        
        stmt.close();
        conn.close();
    } catch (Exception e) {
        
        e.printStackTrace();
    }
    }//GEN-LAST:event_butTambahActionPerformed

